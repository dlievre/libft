#include "libft.h"

int		ft_putbin(unsigned bin)
{
	int mask;
	int n;

	if (!bin)
		return (1);
	mask = 0x8000;
	n = 16;
	while (n-- > 0)
	{
		ft_putchar((mask & bin) == 0x8000 ? '1' : '0');
		bin = bin << 1;
	}
	return (0);
}
