/* ************************************************************************** */
/* ** La fonction strchr() renvoie un pointeur sur  la  première */
/*        occurence du caractère c dans la chaîne s.*/
/* ************************************************************************** */

#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (0);
}
