#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*ps1;
	size_t	i;

	ps1 = s1;
	while (*s1)
		s1++;
	i = 0;
	while (*s2 && i < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = '\0';
	return (ps1);
}
